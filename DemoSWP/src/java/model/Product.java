package model;

/**
 *
 * @author QuangAnh
 */

public class Product {
    private int id;
    private String name;
    private double price;
    private String image;
    private String description;
    private int quantity;
    private String category;

    public Product() {}

    public Product(int id, String name, double price, String image, String description, int quantity, String category) {
        this.id = id; this.name = name; this.price = price; this.image = image;
        this.description = description; this.quantity = quantity; this.category = category;
    }
    
    
    // Getter v√† Setter

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }
    
    @Override
public String toString() {
    return "Product{" +
            "id=" + id +
            ", name='" + name + '\'' +
            ", description='" + description + '\'' +
            ", price=" + price +
            ", image='" + image + '\'' +
            '}';
}
}

